/**
 * Đồng bộ config/app-config.js → extension + web + landing + manifest.json
 * Chạy: node scripts/sync-app-config.js (hoặc prestart server)
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");

/** Nạp .env trước khi require config — để APP_ORIGIN / NEWS_ORIGIN từ env có hiệu lực khi sync */
function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return;
  for (const line of fs.readFileSync(filePath, "utf8").split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx < 1) continue;
    const key = trimmed.slice(0, idx).trim();
    const val = trimmed.slice(idx + 1).trim();
    if (key && process.env[key] == null) process.env[key] = val;
  }
}

loadEnvFile(path.join(root, "server", ".env"));
loadEnvFile(path.join(root, "landing", "server", ".env"));

const srcConfig = path.join(root, "config", "app-config.js");
const cfg = require(srcConfig);

function stripSlash(o) {
  return String(o || "").replace(/\/+$/, "");
}

function originToHostPattern(origin) {
  const u = new URL(origin.replace(/\/$/, ""));
  if (u.hostname === "localhost" || u.hostname === "127.0.0.1") {
    return `${u.protocol}//${u.host}/*`;
  }
  return `${u.protocol}//${u.hostname}/*`;
}

function collectWebUrlPatterns(...origins) {
  const patterns = new Set();
  for (const origin of origins) {
    const base = stripSlash(origin);
    if (!base) continue;
    patterns.add(originToHostPattern(base));
    if (base.includes("localhost")) {
      patterns.add(originToHostPattern(base.replace("localhost", "127.0.0.1")));
    } else if (base.includes("127.0.0.1")) {
      patterns.add(originToHostPattern(base.replace("127.0.0.1", "localhost")));
    }
  }
  // Dev local luôn được phép
  patterns.add("http://localhost:3000/*");
  patterns.add("http://127.0.0.1:3000/*");
  patterns.add("http://localhost:3001/*");
  patterns.add("http://127.0.0.1:3001/*");
  return [...patterns];
}

/** File browser-safe: không phụ thuộc process.env lúc load trong HTML */
function buildBrowserConfigSource() {
  const app = stripSlash(cfg.APP_ORIGIN);
  const news = stripSlash(cfg.NEWS_ORIGIN || cfg.APP_ORIGIN);
  const search = stripSlash(cfg.SEARCH_ORIGIN || cfg.APP_ORIGIN);
  return `/**
 * AUTO-GENERATED by scripts/sync-app-config.js — đừng sửa tay.
 * Sửa nguồn: config/app-config.js rồi chạy sync.
 */
const TIMDIEMBAN_CONFIG = {
  APP_ORIGIN: ${JSON.stringify(app)},
  NEWS_ORIGIN: ${JSON.stringify(news)},
  SEARCH_ORIGIN: ${JSON.stringify(search)},
  MAPS_AUTO_FOCUS_MINUTES: ${Number(cfg.MAPS_AUTO_FOCUS_MINUTES) || 2},
  MAPS_AUTO_REOPEN_MAX: ${Number(cfg.MAPS_AUTO_REOPEN_MAX) || 5}
};
if (typeof module !== "undefined" && module.exports) {
  module.exports = TIMDIEMBAN_CONFIG;
}
if (typeof globalThis !== "undefined") {
  globalThis.TIMDIEMBAN_CONFIG = TIMDIEMBAN_CONFIG;
}
`;
}

function syncCopies() {
  const browserSrc = buildBrowserConfigSource();
  const dests = [
    path.join(root, "extension", "app-config.js"),
    path.join(root, "web", "app-config.js"),
    path.join(root, "landing", "web", "app-config.js"),
    path.join(root, "landing", "config", "app-config.browser.js")
  ];
  for (const dest of dests) {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.writeFileSync(dest, browserSrc, "utf8");
    console.log("[sync-config] →", path.relative(root, dest));
  }

  // Node landing config giữ env-aware (copy nguồn)
  const landingNodeCfg = path.join(root, "landing", "config", "app-config.js");
  fs.writeFileSync(
    landingNodeCfg,
    `/**
 * AUTO-SYNC hint — server landing đọc NEWS/SEARCH từ file này + process.env.
 * Nguồn chính: config/app-config.js (root).
 */
const rootCfg = (() => {
  try {
    return require("../../config/app-config.js");
  } catch {
    return {};
  }
})();

function env(name, fallback) {
  const v = process.env[name];
  return v != null && String(v).trim() ? String(v).trim() : fallback;
}

const FINDMAP_NEWS_CONFIG = {
  NEWS_ORIGIN: env("NEWS_ORIGIN", rootCfg.NEWS_ORIGIN || "http://localhost:3001"),
  SEARCH_ORIGIN: env(
    "SEARCH_ORIGIN",
    rootCfg.SEARCH_ORIGIN || rootCfg.APP_ORIGIN || "http://localhost:3000"
  ),
  MYSQL_DATABASE: env("MYSQL_DATABASE", "findmap_news")
};

module.exports = FINDMAP_NEWS_CONFIG;
if (typeof globalThis !== "undefined") {
  globalThis.FINDMAP_NEWS_CONFIG = FINDMAP_NEWS_CONFIG;
}
`,
    "utf8"
  );
  console.log("[sync-config] →", path.relative(root, landingNodeCfg));
}

function syncManifest() {
  const manifestPath = path.join(root, "extension", "manifest.json");
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  const patterns = collectWebUrlPatterns(cfg.APP_ORIGIN, cfg.NEWS_ORIGIN);

  manifest.host_permissions = ["https://www.google.com/maps/*", ...patterns];

  const bridge = manifest.content_scripts.find((s) => (s.js || []).includes("web-bridge.js"));
  if (bridge) {
    bridge.matches = patterns;
  }

  manifest.description = `Cào dữ liệu Google Maps — điều khiển từ ${stripSlash(cfg.APP_ORIGIN)}`;

  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + "\n", "utf8");
  console.log("[sync-config] manifest host_permissions:", patterns.join(", "));
}

syncCopies();
syncManifest();
console.log("[sync-config] APP_ORIGIN  =", cfg.APP_ORIGIN);
console.log("[sync-config] NEWS_ORIGIN =", cfg.NEWS_ORIGIN);
console.log("[sync-config] SEARCH_ORIGIN =", cfg.SEARCH_ORIGIN || cfg.APP_ORIGIN);
